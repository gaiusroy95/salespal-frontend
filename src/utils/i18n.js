/**
 * Get translation for a given language code and key path.
 * Falls back to English if the key is not found.
 *
 * @param {string} lang - Language code (e.g. 'en', 'hi')
 * @param {string} keyPath - Dot-separated key path (e.g. 'nav.dashboard')
 * @returns {string}
 */
export function t(lang, keyPath) {
    const keys = keyPath.split('.');
    let result = translations[lang];
    for (const key of keys) {
        result = result?.[key];
        if (result === undefined) break;
    }
    if (result !== undefined) return result;

    // Fallback to English
    let fallback = translations['en'];
    for (const key of keys) {
        fallback = fallback?.[key];
        if (fallback === undefined) break;
    }
    return fallback ?? keyPath;
}
